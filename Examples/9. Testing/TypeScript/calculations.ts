module Calculations
{
    export class SimpleMath
    {
        /**
         * Takes two numbers and returns those addiction.
         *
         * @param {number} a
         * @param {number} b
         *
         * @return {number}
         */
        public addTwoNumbers(a:number, b:number):number
        {
            return a + b;
        }
    }
}